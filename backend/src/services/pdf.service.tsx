interface PdfProps {
  doc:       IDocument;
  project:   { name: string; clientName: string };
  reqMap:    Map<string, IRequirement>;
  logoPath?: string;
  companyName: string;
}

function PageHeader({ title, companyName }: { title: string; companyName: string }) {
  return (
    <View style={styles.header} fixed>
      <Text style={styles.headerText}>{companyName} — Confidential</Text>
      <Text style={styles.headerText}>{title}</Text>
    </View>
  );
}

function PageFooter({ status }: { status: string }) {
  return (
    <View style={styles.footer} fixed>
      <Text style={styles.footerText}>Generated by NEXUS — {new Date().toLocaleDateString()}</Text>
      <Text style={styles.pageNumber} render={({ pageNumber, totalPages }) => `${pageNumber} / ${totalPages}`} fixed />
      <Text style={styles.footerText}>{status.toUpperCase()}</Text>
    </View>
  );
}

function SrsDocument({ doc, project, reqMap, logoPath, companyName }: PdfProps) {
  const isApproved = doc.status === 'approved';
  const watermarkText = isApproved ? 'APPROVED' : 'DRAFT';

  const logoData = logoPath && fs.existsSync(logoPath)
    ? `data:image/png;base64,${fs.readFileSync(logoPath).toString('base64')}`
    : null;

  return (
    <Document title={doc.title} author={companyName}>
      {/* Cover page */}
      <Page size="A4" style={styles.coverPage}>
        <View style={styles.coverHeader}>
          {logoData
            ? <Image src={logoData} style={styles.coverLogo} />
            : <Text style={styles.coverLogoText}>{companyName}</Text>
          }
          <Text style={{ fontSize: 10, color: 'rgba(255,255,255,0.7)' }}>NEXUS SDLC Platform</Text>
        </View>

        <View style={styles.coverBody}>
          <Text style={styles.coverTitle}>{doc.title}</Text>
          <Text style={styles.coverSubtitle}>{doc.type.toUpperCase().replace('_', ' ')}</Text>

          <View style={styles.coverMeta}>
            {[
              ['Project',   project.name],
              ['Client',    project.clientName],
              ['Version',   doc.version],
              ['Status',    doc.status.replace('_', ' ').toUpperCase()],
              ['Date',      new Date().toLocaleDateString()],
            ].map(([label, value]) => (
              <View key={label} style={styles.coverMetaRow}>
                <Text style={styles.coverMetaLabel}>{label}</Text>
                <Text style={styles.coverMetaValue}>{value}</Text>
              </View>
            ))}
          </View>
        </View>

        <View style={styles.coverFooter}>
          <Text style={styles.coverFooterText}>Confidential — {companyName}</Text>
          <Text style={styles.coverFooterText}>{isApproved ? '✓ Approved Document' : 'Draft — Subject to Change'}</Text>
        </View>
      </Page>

      {/* Table of Contents */}
      <Page size="A4" style={styles.page}>
        <PageHeader title={doc.title} companyName={companyName} />
        <Text style={styles.watermark}>{watermarkText}</Text>
        <Text style={styles.sectionTitle}>Table of Contents</Text>
        <View style={styles.toc}>
          {doc.sections.map((s, i) => (
            <View key={s.id} style={styles.tocRow}>
              <Text style={styles.tocTitle}>{i + 1}. {s.title}</Text>
              <Text style={styles.tocPage}>—</Text>
            </View>
          ))}
        </View>
        <PageFooter status={doc.status} />
      </Page>

      {/* Content pages */}
      {doc.sections.map((section: IDocSection, i: number) => (
        <Page key={section.id} size="A4" style={styles.page}>
          <PageHeader title={doc.title} companyName={companyName} />
          <Text style={styles.watermark}>{watermarkText}</Text>

          <Text style={styles.sectionTitle}>{i + 1}. {section.title}</Text>

          {section.content && (
            <Text style={styles.sectionContent}>{section.content}</Text>
          )}

          {section.linkedReqs?.length > 0 && (
            <View>
              <Text style={{ fontSize: 10, fontWeight: 'bold', color: '#555', marginBottom: 6 }}>
                Linked Requirements
              </Text>
              {section.linkedReqs.map((rid) => {
                const req = reqMap.get(rid.toString());
                if (!req) return null;
                return (
                  <View key={rid.toString()} style={styles.reqBlock}>
                    <Text style={styles.reqId}>{req.reqId}</Text>
                    <Text style={styles.reqTitle}>{req.title}</Text>
                    <Text style={styles.reqMeta}>{req.type} · {req.priority} · {req.status}</Text>
                    {req.acceptanceCriteria && (
                      <Text style={{ ...styles.reqMeta, marginTop: 4 }}>AC: {req.acceptanceCriteria}</Text>
                    )}
                  </View>
                );
              })}
            </View>
          )}

          <PageFooter status={doc.status} />
        </Page>
      ))}
    </Document>
  );
}

export async function generateSrsPdf(
  doc: IDocument,
  project: { name: string; clientName: string },
  reqMap: Map<string, IRequirement>,
  settings: { logoPath?: string; companyName: string },
): Promise<Buffer> {
  const element = React.createElement(SrsDocument, {
    doc, project, reqMap,
    logoPath: settings.logoPath,
    companyName: settings.companyName,
  });
  return await renderToBuffer(element);
}

