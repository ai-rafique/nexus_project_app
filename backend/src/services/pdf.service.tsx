  coverPage: { fontFamily: 'Helvetica', fontSize: 10, color: '#1a1a2e', padding: 0, display: 'flex', flexDirection: 'column' },
  coverHeader: { backgroundColor: '#0f3460', padding: 40, flexDirection: 'row', alignItems: 'center', justifyContent: 'space-between' },
  coverLogo: { width: 80, height: 40, objectFit: 'contain' },
  coverLogoText: { fontSize: 22, fontWeight: 'bold', color: '#fff' },
  coverBody: { padding: 50, flex: 1 },
  coverTitle: { fontSize: 28, fontWeight: 'bold', color: '#0f3460', marginBottom: 12 },
  coverSubtitle: { fontSize: 14, color: '#555', marginBottom: 40 },
  coverMeta: { marginTop: 8 },
  coverMetaRow: { flexDirection: 'row', marginBottom: 6 },
  coverMetaLabel: { width: 100, fontSize: 10, color: '#888' },
  coverMetaValue: { fontSize: 10, color: '#1a1a2e', fontWeight: 'bold' },
  coverFooter: { backgroundColor: '#f0f4ff', padding: 20, flexDirection: 'row', justifyContent: 'space-between', alignItems: 'center' },
  coverFooterText: { fontSize: 9, color: '#666' },
  // Watermark
  watermark: { position: 'absolute', top: '40%', left: '10%', fontSize: 72, color: '#e5e7eb', opacity: 0.3, transform: 'rotate(-35deg)' },
  // Header/Footer
